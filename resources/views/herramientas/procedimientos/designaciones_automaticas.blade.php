    </ol>

    <h5 class="fw-bold mb-3">1. Introducción</h5>
    <p>
        La designación masiva de cargos interinos es una tarea anual fundamental que realiza la Dirección General de Personal de la UNCa. Permite renovar aproximadamente 1200 cargos interinos, evitando la carga individual y garantizando agilidad, eficiencia y cumplimiento normativo.
    </p>

    <h5 class="fw-bold mt-4 mb-3">2. Alcance</h5>
    <p>
        Aplica a cargos docentes interinos con baja el 31 de marzo y resolución de renovación. Se realiza en abril antes de la baja efectiva.
    </p>

    <h5 class="fw-bold mt-4 mb-3">3. Objetivo</h5>
    <p>
        Renovar masivamente cargos docentes interinos mediante el módulo de renovación de SIU-Mapuche, optimizando tiempos y reduciendo errores.
    </p>

    <h5 class="fw-bold mt-4 mb-3">4. Responsables</h5>
    <ul>
        <li><strong>Dirección General de Personal:</strong> Responsable de la ejecución.</li>
        <li><strong>Personal administrativo:</strong> Operativo en el sistema.</li>
        <li><strong>Facultades / Secretarías:</strong> Emisión de resoluciones de renovación.</li>
    </ul>

    <h5 class="fw-bold mt-4 mb-3">5. Recursos y materiales necesarios</h5>
    <ul>
        <li>Acceso al módulo SIU-Mapuche: <strong>Servicios → Cargos → Renovación</strong>.</li>
        <li>Resoluciones de renovación en formato físico, selladas y firmadas.</li>
        <li>Usuario habilitado con permisos.</li>
    </ul>

    <h5 class="fw-bold mt-4 mb-3">6. Procedimiento paso a paso</h5>
    <p>
        Debe realizarse entre el 1 y el 10 de abril, con sistema y resolución disponibles.
    </p>

    <h6 class="mt-3">Paso 1: Ingreso al sistema</h6>
    <ol>
        <li>Ingresar a SIU-Mapuche → Servicios → Cargos → Renovación.</li>
    </ol>

    <h6 class="mt-3">Paso 2: Filtrado de cargos</h6>
    <ol start="2">
        <li>Filtrar por fecha de baja: <strong>31/03/[año]</strong>.</li>
        <li>Seleccionar la dependencia correspondiente.</li>
    </ol>

    <h6 class="mt-3">Paso 3: Identificación según resolución</h6>
    <ol start="4">
        <li>Verificar resolución de renovación.</li>
        <li>Tildar cargos indicados en la misma.</li>
        <li>Presionar <strong>Siguiente</strong>.</li>
    </ol>

    <h6 class="mt-3">Paso 4: Preparación</h6>
    <ol start="7">
        <li>Destildar "sin crear cargo".</li>
        <li>Completar:
            <ul>
                <li><strong>Fecha Alta:</strong> 01/04/[año]</li>
                <li><strong>Fecha Baja:</strong> 31/03/[año siguiente]</li>
                <li><strong>Tipo de Norma:</strong> Resolución</li>
                <li><strong>Emisor:</strong> Consejo Académico / Decano / Rector</li>
                <li><strong>Número y Fecha de Norma</strong></li>
            </ul>
        </li>
    </ol>

    <h6 class="mt-3">Paso 5: Confirmación</h6>
    <ol start="9">
        <li>Presionar <strong>Renovar Cargos</strong>.</li>
    </ol>

    <p>El sistema mostrará los cargos renovados y los no renovados para revisión inmediata.</p>

    <h5 class="fw-bold mt-4 mb-3">7. Posibles errores y medidas preventivas</h5>
    <div class="table-responsive mb-3">
        <table class="table table-bordered table-striped">
            <thead class="table-light">
                <tr>
                    <th>Error Común</th>
                    <th>Prevención / Solución</th>
                </tr>
            </thead>
            <tbody>
                <tr>
                    <td>Tildar un cargo incorrecto</td>
                    <td>Verificar con la resolución antes de tildar.</td>
                </tr>
                <tr>
                    <td>Olvidar destildar "sin crear cargo"</td>
                    <td>Confirmar que se cree nuevo cargo en cada carga.</td>
                </tr>
                <tr>
                    <td>Fechas mal ingresadas</td>
                    <td>Revisar alta, baja y norma antes de confirmar.</td>
                </tr>
                <tr>
                    <td>Datos incorrectos de la resolución</td>
                    <td>Corroborar con el documento oficial.</td>
                </tr>
                <tr>
                    <td>Omisión de cargos</td>
                    <td>Comparar resolución con cargos renovados.</td>
                </tr>
            </tbody>
        </table>
    </div>

    <h5 class="fw-bold mt-4 mb-3">8. Control posterior a la renovación</h5>
    <ul>
        <li>Verificar que todos los cargos de la resolución fueron renovados.</li>
    </ul>
    <p>Usar: <strong>Informes → Cargos → Cargos Baja</strong></p>
    <ul>
        <li><strong>Fecha:</strong> 31/03/[año]</li>
        <li><strong>Dependencia:</strong> la correspondiente</li>
    </ul>
    <p>Comparar el listado con la resolución y analizar diferencias:</p>
    <ul>
        <li><strong>No renovados justificados:</strong> emitir parte de baja.</li>
        <li><strong>No renovados por error:</strong> verificar si fue omisión en tildado o ausencia en resolución.</li>
    </ul>

    <h5 class="fw-bold mt-4 mb-3">9. Observaciones</h5>
    <ul>
        <li><strong>Licencias por maternidad:</strong> deben cortarse al 31/03 y generarse nueva desde 01/04.</li>
        <li><strong>Licencias médicas:</strong> no se cargan si el cargo está dado de baja → renovar primero.</li>
        <li><strong>Licencias sin goce (ETC):</strong> el sistema las transfiere automáticamente si estaban vigentes.</li>
    </ul>
    <p>Estas condiciones garantizan una gestión sin errores ni rechazos por parte del sistema.</p>
</div>
