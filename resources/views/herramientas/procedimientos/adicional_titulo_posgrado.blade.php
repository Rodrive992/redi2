<div class="container px-3 py-4 bg-white border rounded shadow-sm">
    <h4 class="mb-4 text-primary fw-bold">
        <i class="bi bi-award-fill me-2"></i>
        Solicitud de Adicional por Título de Posgrado
    </h4>

    <p><strong>Destinado a:</strong> Personal docente de los niveles universitario y preuniversitario de la Universidad Nacional de Catamarca.</p>
    <p><strong>Gestionado por:</strong> Dirección General de Personal – UNCa.</p>

    <h5 class="fw-bold mt-4 mb-2">1. Descripción</h5>
    <p>
        El adicional por título de posgrado está destinado al personal que revista en cargos del escalafón docente, tanto en el nivel de enseñanza universitario como preuniversitario, y se otorga en función del título de posgrado de mayor jerarquía (Doctorado, Maestría o Especialización).
    </p>
    <p>El adicional se paga por cada cargo en el que el/la docente revista.</p>
    <p><strong>Importante:</strong> el pago del adicional se realiza a mes vencido, lo que significa que la documentación presentada se procesa durante el mes siguiente, y el pago se efectiviza en la liquidación correspondiente a ese período.</p>

    <h5 class="fw-bold mt-4 mb-2">2. Requisitos del Título</h5>
    <ul>
        <li><strong>Títulos nacionales:</strong> deben contar con reconocimiento oficial y validez nacional (Ley N° 24.521), y estar acreditados por la CONEAU (Comisión Nacional de Evaluación y Acreditación Universitaria).</li>
        <li><strong>Títulos extranjeros:</strong> deben contar con legalización mediante Apostillado de La Haya o similar, estar acreditados por organismo equivalente a la CONEAU, o contar con constancia de validación vigente en el país de origen. En caso necesario, traducidos al español.</li>
    </ul>

    <h5 class="fw-bold mt-4 mb-2">3. Documentación a presentar</h5>
    <p>Para gestionar el pago del adicional, el/la docente debe presentar en la Dirección General de Personal:</p>
    <ul>
        <li>
            <strong>Título de posgrado</strong> (una de las siguientes opciones):
            <ol>
                <li>Original (será devuelto luego de la autenticación) + copia simple autenticada.</li>
                <li>Copia autenticada del título.</li>
                <li>Certificado de título en trámite + Certificado analítico (en caso de no contar con el título definitivo).</li>
            </ol>
        </li>
        <li>Resolución ministerial que otorga el reconocimiento oficial del título.</li>
        <li>Resolución de la CONEAU que acredita el posgrado.</li>
    </ul>
    <p>La Dirección General de Personal requiere esta documentación completa y en tiempo oportuno para incluir la información en la liquidación del mes siguiente, respetando el carácter de pago a mes vencido.</p>

    <h5 class="fw-bold mt-4 mb-2">4. Observaciones</h5>
    <ul>
        <li>La autenticación de la copia del título puede ser realizada por la Dirección General de Personal si se presenta el original.</li>
        <li>No es necesario presentar ambas opciones (título y certificado en trámite): basta con una alternativa válida junto a la documentación complementaria exigida.</li>
    </ul>

