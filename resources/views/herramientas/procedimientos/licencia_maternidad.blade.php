<div class="container px-3 py-4 bg-white border rounded shadow-sm">
    <h4 class="text-primary fw-bold mb-2">
        <i class="bi bi-person-bounding-box me-2"></i>
        Procedimiento para Licencia por Maternidad
    </h4>
    <h6 class="mb-4 text-secondary">
        Dirección General de Personal – Universidad Nacional de Catamarca
    </h6>

    <h5 class="fw-bold mb-2">1. Objetivo</h5>
    <p>
        Establecer los pasos que debe seguir el personal Docente y Nodocente de la UNCA para gestionar la licencia por maternidad y percibir la asignación correspondiente abonada por ANSES, conforme a la normativa vigente.
    </p>

    <h5 class="fw-bold mt-4 mb-2">2. Alcance</h5>
    <p>
        Aplica a todo el personal Docente y Nodocente de la UNCA, sin distinción de carácter (permanente, interino, contratado, etc.).
    </p>

    <h5 class="fw-bold mt-4 mb-2">3. Base legal</h5>
    <ul>
        <li><strong>Personal Docente:</strong> Decreto N.º 1246/2015 – Art. 8, inc. a.</li>
        <li><strong>Personal Nodocente:</strong> Resolución Rectoral N.º 172/2001 – Art. 9, inc. g.</li>
        <li><strong>Asignaciones familiares (ANSES):</strong> Decreto N.º 1668/2012 – Art. 6.</li>
    </ul>

    <h5 class="fw-bold mt-4 mb-2">4. Requisitos y plazos</h5>
    <p>El trámite debe iniciarse <strong>entre 60 y 30 días antes</strong> de la fecha probable de parto.</p>
    <p>Fuera de este plazo, la UNCA abonará la licencia sin intervención de ANSES, quedando el trámite sin constancia formal en dicho organismo.</p>
    <p>Solo se requiere el formulario PS.2.55 – <strong>“Declaración Jurada de Novedades Unificadas – SUAF”</strong>.</p>

    <h5 class="fw-bold mt-4 mb-2">5. Responsables</h5>
    <ul>
        <li><strong>Agente gestante:</strong> inicia y completa el trámite ante ANSES.</li>
        <li><strong>Médico particular:</strong> firma y completa el rubro 6 del formulario.</li>
        <li><strong>Área de Licencias – DGP:</strong> controla y certifica la documentación.</li>
        <li><strong>Directora de Personal:</strong> firma el rubro 9 del formulario PS.2.55.</li>
    </ul>

    <h5 class="fw-bold mt-4 mb-2">6. Procedimiento</h5>

    <div class="step mb-3">
        <h6 class="fw-bold">1. Descarga del formulario</h6>
        <p>
            Descargar el formulario PS.2.55 desde la web de ANSES o retirarlo en una oficina.
        </p>
    </div>

    <div class="step mb-3">
        <h6 class="fw-bold">2. Solicitud de turno en ANSES</h6>
        <p>Solicitar un turno para presentar el formulario ante ANSES.</p>
    </div>

    <div class="step mb-3">
        <h6 class="fw-bold">3. Completar formulario PS.2.55</h6>
        <ul>
            <li><strong>Rubro 1:</strong> Datos personales de la agente.</li>
            <li><strong>Rubro 2:</strong> Datos del otro progenitor.</li>
            <li><strong>Rubro 3:</strong> Datos del empleador (CUIL y razón social UNCA).</li>
            <li><strong>Rubro 5:</strong> Fecha en que se declara el embarazo (debe coincidir con rubro 6).</li>
            <li><strong>Rubro 6:</strong> Completado por médico particular.</li>
            <li><strong>Rubro 9:</strong> Firmas del progenitor, la agente y la Directora de Personal.</li>
        </ul>
    </div>

    <div class="step mb-3">
        <h6 class="fw-bold">4. Presentación interna en DGP</h6>
        <p>Presentar el formulario completo en el Área de Licencias. Allí se verifica y se firma por la Directora de Personal.</p>
    </div>

    <div class="step mb-3">
        <h6 class="fw-bold">5. Presentación en ANSES</h6>
        <p>Entregar formulario en ANSES con la documentación correspondiente. ANSES emite la constancia de Alta por Maternidad con fechas oficiales.</p>
    </div>

    <div class="step mb-3">
        <h6 class="fw-bold">6. Entrega de constancia</h6>
        <ul>
            <li><strong>Original:</strong> se entrega en la Dirección General de Personal.</li>
            <li><strong>Copia:</strong> se presenta en el Servicio Médico de la UNCA.</li>
        </ul>
    </div>

    <div class="step">
        <h6 class="fw-bold">7. Comunicación al agente</h6>
        <p>
            El Área de Licencias informa a la agente que, desde la fecha indicada en la constancia de ANSES, los haberes serán percibidos directamente por ANSES.
        </p>
    </div>
</div>